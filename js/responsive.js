var responsive = function() {
    var x = document.getElementById('list-overmap');
    if (x.style.display === 'none') {
        x.style.display = 'block';
    } else {
        x.style.display = 'none';
    }

    var y = document.getElementById('hamburger-icon');
    if (y.style.display === 'none') {
        x.style.display = 'block';
    }

};
